const mongoose = require('mongoose');

// User Schema
const UserSchema = new mongoose.Schema({
  first_name: {
    type: String,
    required: true,
  },
  last_name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
});

const User = (module.exports = mongoose.model('user', UserSchema));
